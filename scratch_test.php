<?php
// Simple script to find Vietnamese strings in HTML text nodes
$dir = new RecursiveIteratorIterator(new RecursiveDirectoryIterator('public'));
$count = 0;
foreach ($dir as $file) {
    if ($file->getExtension() === 'php') {
        $content = file_get_contents($file->getPathname());
        // find text between tags containing vietnamese chars
        if (preg_match_all('/>([^<]*[Šŗ?„?a?????‚?????ťŤ???Í?????Ūž?i?ůÚ?ű?Ű?????o?????ķý?u?u?????ż????dŃņ?√?A?????¬?????…»??? ?????ÕŐ?I?”“?’?‘?????O?????ŕŔ?U?U?????›????–]+[^<]*)</u', $content, $matches)) {
            $count += count($matches[1]);
        }
    }
}
echo "Total Vietnamese text blocks found: $count\n";
